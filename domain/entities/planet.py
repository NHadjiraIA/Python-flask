
class Planet(object):
    def __init__(self, planet_id=None, planet_name=None, planet_type=None, home_star=None, mass=None, radius=None,
                 distance=None):
        self.planet_id = planet_id
        self.planet_name = planet_name
        self.planet_type = planet_type
        self.home_star = home_star
        self.mass = mass
        self.radius = radius
        self.distance = distance
